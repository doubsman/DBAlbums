	`PLAYLIST` TEXT, 
	`PATH` TEXT, 
	`FILENAME` TEXT, 
	`NAME` TEXT,
	`ALBUM` TEXT,
	`ARTIST` TEXT, 
	`TITTLE` TEXT, 
	`ADD` DATETIME);
CREATE INDEX FOOBAR_ndx_FILENAME ON DBFOOBAR(FILENAME);


CREATE TABLE FOOBOR(
	FILENAME TEXT,
	FILENAMEM TEXT);


CREATE TABLE `COVERS` (
	`ID_CD` INT,
	`NAME` TEXT,
	`COVER` BLOB,
	`THUMBNAIL` BLOB);
CREATE INDEX COVERS_ndx_IDCD ON COVERS(`ID_CD`);


CREATE VIEW VW_COMPLETION AS
SELECT ID_CD, ARTIST AS SYNTHAX FROM ALBUMS WHERE ARTIST <> '' AND ARTIST <> 'Various'
UNION
SELECT ID_CD, LABEL FROM ALBUMS WHERE LABEL <> ''
UNION 
SELECT ID_CD, SUBPOSITION FROM ALBUMS WHERE FAMILY='Artists'
